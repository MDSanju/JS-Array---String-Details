// using toLowerCase() to convert all characters lower case
const userInput = 'BlacKpiNK';
const saveUserName = 'blackPink';
if (userInput.toLowerCase() == saveUserName.toLowerCase()) {
    console.log('both are matched');
} else {
    console.log('are not matched');
}


// using toUpperCase() to convert all characters upper case
const country = 'caNAda';
const lovelyWeather = 'canada';
if (country.toUpperCase() == lovelyWeather.toUpperCase()) {
    console.log('will go to Canada');
} else {
    console.log('cant go there');
}

